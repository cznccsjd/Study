package Bilibili2111.controlflow;

public class SwitchDemo {
    public static void main(String[] args){
        int i = 0;
        switch(i){
            case 0:
                System.out.println("case0执行");
                break;
            case 1:
                System.out.println("case1执行");
                break;
            case 2:
                System.out.println("case2执行");
                break;
            default:
                System.out.println("default执行");
    }
    }
}
