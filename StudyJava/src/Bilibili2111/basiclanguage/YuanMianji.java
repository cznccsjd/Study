package Bilibili2111.basiclanguage;
/*
学习定义常量和变量
 */
public class YuanMianji {
    public static final double PI = 3.14; //定义一个表示PI的常量
    public static void main(String[] args) {
        int r = 5;  // 定义一个表示半径的变量
        double ymj = PI * r * r;  // 计算圆的面积
        System.out.println("圆的面积等于" + ymj);
    }
}
